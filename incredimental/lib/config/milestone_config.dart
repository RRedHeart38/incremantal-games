  static List<MilestoneDefinition> definitionsFor(String key) => milestones[key] ?? const [];

  static double cumulativeMultiplier(
    String key,
    int currentLevel, {
    bool includeGlobal = true,
    Set<String>? types,
  }) {
    final applicableMilestones = <MilestoneDefinition>[];
    if (includeGlobal) {
      applicableMilestones.addAll(definitionsFor('all'));
    }
    applicableMilestones.addAll(definitionsFor(key));
    applicableMilestones.sort((left, right) => left.targetLevel.compareTo(right.targetLevel));

    double multiplier = 1.0;
    for (final milestone in applicableMilestones) {
      if (currentLevel >= milestone.targetLevel && (types == null || types.contains(milestone.type))) {
        multiplier *= milestone.multiplier;
      }
    }
    return multiplier;
  }

  static double totalMilestoneMultiplier(String key, int currentLevel, {bool includeGlobal = true}) {
    return cumulativeMultiplier(key, currentLevel, includeGlobal: includeGlobal);
  }
